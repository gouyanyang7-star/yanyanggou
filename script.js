// ========== Enter 界面转场逻辑 ==========
document.addEventListener('DOMContentLoaded', function() {
    const enterScreen = document.getElementById('enter-screen');
    const enterBtn = document.getElementById('enter-btn');
    const homePage = document.getElementById('home-page');

    enterBtn.addEventListener('click', function() {
        enterScreen.classList.add('fade-out');
        setTimeout(() => {
            enterScreen.style.display = 'none';
            homePage.classList.add('visible');
        }, 1500);
    });

    // ========== Home 留言板功能 ==========
    const homeMessageInput = document.getElementById('home-message-input');
    const homeSendMessage = document.getElementById('home-send-message');
    const homeMessageList = document.getElementById('home-message-list');

    if (homeMessageInput && homeSendMessage) {
        loadMessages('home_messages', homeMessageList);

        homeSendMessage.addEventListener('click', function() {
            const text = homeMessageInput.value.trim();
            if (text) {
                addMessage(text, 'home_messages', homeMessageList);
                homeMessageInput.value = '';
            }
        });

        homeMessageInput.addEventListener('keypress', function(e) {
            if (e.key === 'Enter' && !e.shiftKey) {
                e.preventDefault();
                homeSendMessage.click();
            }
        });
    }

    // ========== Tab 切换逻辑 ==========
    const navItems = document.querySelectorAll('.nav-item');
    const sections = document.querySelectorAll('.section');

    navItems.forEach(item => {
        item.addEventListener('click', function(e) {
            e.preventDefault();
            navItems.forEach(nav => nav.classList.remove('active'));
            sections.forEach(sec => {
                sec.classList.remove('active-tab');
                sec.style.opacity = '0';
            });

            this.classList.add('active');
            const targetId = this.getAttribute('href').substring(1);
            const targetSection = document.getElementById(targetId);

            if (targetSection) {
                targetSection.classList.add('active-tab');
                setTimeout(() => {
                    targetSection.style.opacity = '1';
                }, 50);
                window.scrollTo({ top: 0, behavior: 'smooth' });
            }
        });
    });

    // ========== Home 卡片点击跳转 ==========
    const homeNavItems = document.querySelectorAll('.home-nav-item');
    homeNavItems.forEach(item => {
        item.addEventListener('click', function() {
            const target = this.getAttribute('data-target');
            const targetNav = document.querySelector(`.nav-item[href="#${target}"]`);
            if (targetNav) {
                targetNav.click();
            }
        });
    });

    // ========== Lightbox 功能 ==========
    window.openLightbox = function(src) {
        const lightbox = document.getElementById('lightbox');
        const lightboxImg = document.getElementById('lightbox-img');
        lightboxImg.src = src;
        lightbox.classList.add('active');
        document.body.style.overflow = 'hidden';
    };

    window.closeLightbox = function() {
        const lightbox = document.getElementById('lightbox');
        lightbox.classList.remove('active');
        document.body.style.overflow = '';
    };

    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            closeLightbox();
            closeArticleModal();
        }
    });

    const lightbox = document.getElementById('lightbox');
    if (lightbox) {
        lightbox.addEventListener('click', closeLightbox);
    }
});

