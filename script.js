// ========== 留言板辅助函数 ==========
function addMessage(text, storageKey, listElement) {
    const messages = getMessages(storageKey);
    const message = {
        id: Date.now(),
        text: text,
        date: new Date().toLocaleString('zh-CN')
    };
    messages.push(message);
    localStorage.setItem(storageKey, JSON.stringify(messages));
    displayMessage(message, listElement);
}

function getMessages(storageKey) {
    const stored = localStorage.getItem(storageKey);
    return stored ? JSON.parse(stored) : [];
}

function loadMessages(storageKey, listElement) {
    const messages = getMessages(storageKey);
    messages.forEach(msg => displayMessage(msg, listElement));
}

function displayMessage(message, listElement) {
    const msgDiv = document.createElement('div');
    msgDiv.className = 'message-item';
    msgDiv.style.cssText = 'padding: 0.8rem 0; border-bottom: 1px solid var(--gold-dim); font-size: 0.85rem; color: var(--text-secondary);';
    msgDiv.innerHTML = `${message.text}<br><small style="opacity: 0.5; font-size: 0.75rem;">${message.date}</small>`;
    listElement.insertBefore(msgDiv, listElement.firstChild);
}

